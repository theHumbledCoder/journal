/**
 * Runs before `vite build`. Scans public/images/gallery/ and writes
 * src/generated/gallery-manifest.js so the React app can import the list.
 * Add photos to public/images/gallery/ and they auto-appear on the next build.
 */
import { readdirSync, writeFileSync, mkdirSync, existsSync } from 'fs'
import { join, extname } from 'path'

const ROOT = new URL('..', import.meta.url).pathname.replace(/^\/([A-Z]:)/, '$1')
const galleryDir = join(ROOT, 'public', 'images', 'gallery')
const outDir = join(ROOT, 'src', 'generated')
const outFile = join(outDir, 'gallery-manifest.js')

const IMAGE_EXTS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif', '.avif'])

let images = []
if (existsSync(galleryDir)) {
  images = readdirSync(galleryDir)
    .filter(f => IMAGE_EXTS.has(extname(f).toLowerCase()))
    .sort()
    .map(f => ({
      src: `images/gallery/${f}`,
      alt: f.replace(/\.[^.]+$/, '').replace(/[-_]/g, ' '),
    }))
}

mkdirSync(outDir, { recursive: true })
writeFileSync(
  outFile,
  `// Auto-generated by scripts/generate-manifest.mjs — do not edit by hand\nexport const galleryImages = ${JSON.stringify(images, null, 2)};\n`,
)
console.log(`[manifest] ${images.length} gallery image(s) found`)
